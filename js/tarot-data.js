// ==================== 工具函数 ====================
function getCardById(id) {
  for (var i = 0; i < TAROT_CARDS.length; i++) {
    if (TAROT_CARDS[i].id === id) return TAROT_CARDS[i];
  }
  return null;
}

function getSpreadById(id) {
  return SPREADS[id] || SPREADS.timeFlow;
}

function drawCards(spreadId) {
  var spread = getSpreadById(spreadId);
  var count = spread.cardCount;
  var deck = TAROT_CARDS.slice(); // 复制牌组
  var drawn = [];

  // Fisher-Yates 洗牌
  for (var i = deck.length - 1; i > 0; i--) {
    var j = Math.floor(Math.random() * (i + 1));
    var temp = deck[i];
    deck[i] = deck[j];
    deck[j] = temp;
  }

  // 抽取所需数量的牌
  for (var i = 0; i < count; i++) {
    var card = deck[i];
    var isReversed = Math.random() < 0.5; // 50% 概率逆位
    drawn.push({
      cardId: card.id,
      cardName: card.name,
      cardNameEn: card.nameEn,
      arcana: card.arcana,
      suit: card.suit,
      element: card.element,
      reversed: isReversed,
      keywords: card.keywords,
      meaning: isReversed ? card.reversed.meaning : card.upright.meaning,
      love: isReversed ? card.reversed.love : card.upright.love,
      career: isReversed ? card.reversed.career : card.upright.career,
      finance: isReversed ? card.reversed.finance : card.upright.finance,
      advice: isReversed ? card.reversed.advice : card.upright.advice,
      positionIndex: i
    });
  }

  return drawn;
}

// 将抽牌结果编码为 URL 参数
function encodeCardsToURL(drawn) {
  var parts = [];
  for (var i = 0; i < drawn.length; i++) {
    parts.push(drawn[i].cardId);
    if (drawn[i].reversed) {
      parts.push('r');
    }
  }
  return parts.join(',');
}

// 从 URL 参数解析抽牌结果
function parseCardsFromURL(param) {
  if (!param) return [];
  var parts = param.split(',');
  var result = [];
  var i = 0;
  while (i < parts.length) {
    if (parts[i] === 'r') {
      if (result.length > 0) {
        result[result.length - 1].reversed = true;
      }
      i++;
    } else {
      var cardData = getCardById(parts[i]);
      if (cardData) {
        result.push({
          cardId: cardData.id,
          cardName: cardData.name,
          cardNameEn: cardData.nameEn,
          arcana: cardData.arcana,
          suit: cardData.suit,
          element: cardData.element,
          reversed: false,
          keywords: cardData.keywords,
          positionIndex: result.length
        });
      }
      i++;
    }
  }
  // 填充正逆位含义
  for (var j = 0; j < result.length; j++) {
    var c = getCardById(result[j].cardId);
    if (c) {
      result[j].meaning = result[j].reversed ? c.reversed.meaning : c.upright.meaning;
      result[j].love = result[j].reversed ? c.reversed.love : c.upright.love;
      result[j].career = result[j].reversed ? c.reversed.career : c.upright.career;
      result[j].finance = result[j].reversed ? c.reversed.finance : c.upright.finance;
      result[j].advice = result[j].reversed ? c.reversed.advice : c.upright.advice;
    }
  }
  return result;
}
